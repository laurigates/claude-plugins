#!/usr/bin/env bash
# report/generate.sh — Generate a markdown report from a run's summary.tsv.
set -euo pipefail

SCRIPTS_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
# shellcheck disable=SC1091
source "${SCRIPTS_ROOT}/config.sh"

# Accept RUN_DIR as argument, from env, or fall back to the most recent run.
if [[ -n "${1:-}" ]]; then
  RUN_DIR="$1"
elif [[ -n "${RUN_DIR:-}" ]]; then
  : # already set
else
  RUN_DIR="$(find "${RESULTS_BASE}" -mindepth 1 -maxdepth 1 -type d 2>/dev/null | sort | tail -1)"
  if [[ -z "$RUN_DIR" ]]; then
    echo "Error: no results directory found. Run a diagnostic or benchmark first." >&2
    exit 1
  fi
fi

TSV="${RUN_DIR}/summary.tsv"
REPORT="${RUN_DIR}/report.md"

if [[ ! -f "$TSV" ]]; then
  echo "Error: $TSV not found" >&2
  exit 1
fi

# ── Count results ─────────────────────────────────────────────────────────────
total_pass=0; total_warn=0; total_fail=0
declare -A sect_pass sect_warn sect_fail

while IFS=$'\t' read -r status section message; do
  case "$status" in
    PASS) (( total_pass++ )) || true; sect_pass["$section"]=$(( ${sect_pass["$section"]:-0} + 1 )) ;;
    WARN) (( total_warn++ )) || true; sect_warn["$section"]=$(( ${sect_warn["$section"]:-0} + 1 )) ;;
    FAIL) (( total_fail++ )) || true; sect_fail["$section"]=$(( ${sect_fail["$section"]:-0} + 1 )) ;;
  esac
done < "$TSV"

if (( total_fail > 0 )); then overall="FAIL"; overall_icon="🔴"
elif (( total_warn > 0 )); then overall="WARN"; overall_icon="🟡"
else overall="PASS"; overall_icon="🟢"; fi

run_ts="$(basename "$RUN_DIR")"

# ── Write report ──────────────────────────────────────────────────────────────
{
printf "# macOS Performance Report\n\n"
printf "**Run:** %s  \n" "$run_ts"
printf "**Host:** %s  \n" "$(scutil --get ComputerName 2>/dev/null || hostname)"
printf "**Model:** %s  \n" "$(sysctl -n machdep.cpu.brand_string 2>/dev/null || sysctl -n hw.model 2>/dev/null || echo unknown)"
printf "**macOS:** %s  \n" "$(sw_vers -productVersion 2>/dev/null || echo unknown)"
printf "**RAM:** %s GB  \n" "$(( $(sysctl -n hw.memsize) / 1073741824 ))"
printf "**CPUs:** %s logical  \n\n" "$(sysctl -n hw.logicalcpu)"

printf "## Overall Status: %s %s\n\n" "$overall_icon" "$overall"
printf "| Status | Count |\n|--------|-------|\n"
printf "| 🟢 PASS | %d |\n| 🟡 WARN | %d |\n| 🔴 FAIL | %d |\n\n" "$total_pass" "$total_warn" "$total_fail"

printf "## Section Summary\n\n"
printf "| Section | PASS | WARN | FAIL | Status |\n|---------|------|------|------|--------|\n"
all_sections="$(awk -F'\t' '{print $2}' "$TSV" | sort -u)"
while IFS= read -r s; do
  [[ -z "$s" ]] && continue
  p=${sect_pass["$s"]:-0}; w=${sect_warn["$s"]:-0}; f=${sect_fail["$s"]:-0}
  if (( f > 0 )); then icon="🔴"; elif (( w > 0 )); then icon="🟡"; else icon="🟢"; fi
  printf "| %s | %d | %d | %d | %s |\n" "$s" "$p" "$w" "$f" "$icon"
done <<< "$all_sections"

printf "\n## Detailed Findings\n\n"
current_section=""
while IFS=$'\t' read -r status section message; do
  if [[ "$section" != "$current_section" ]]; then
    printf "\n### %s\n\n" "$section"
    current_section="$section"
  fi
  case "$status" in
    PASS)    printf -- "- ✅ %s\n" "$message" ;;
    WARN)    printf -- "- ⚠️  %s\n" "$message" ;;
    FAIL)    printf -- "- ❌ %s\n" "$message" ;;
    INFO)    printf -- "- ℹ️  %s\n" "$message" ;;
    SECTION) printf "\n**%s**\n" "$message" ;;
  esac
done < "$TSV"

printf "\n---\n*Generated by macos-performance-benchmark — %s*\n" "$(date)"
} > "$REPORT"

cat "$REPORT"
echo ""
echo "Report saved to: $REPORT"
